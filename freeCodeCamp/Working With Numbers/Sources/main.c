#include <math.h>
#include <stdio.h>

int main() {
  int num = 6;

  printf("%.0f\n", 8.9 + 4.5);
  printf("%.0f\n", num + 3.0);
  printf("%.0f\n", pow(5, 3));   // power
  printf("%.0f\n", sqrt(36));    // square root
  printf("%.0f\n", ceil(36.7));  // round up
  printf("%.0f\n", floor(36.7)); // round down

  return 0;
}
